  private String htmlize(String text) {
    return "<html><head></head><body style=\"margin-top: 0; font-family: sans;\">" + text + "</body></html>";
  }

  private void automatonSimplifierChanged(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_automatonSimplifierChanged
    descAutomatonSimplifier.setText(htmlize(
            ModuleSelectionHelper.lookupImpl(AutomatonSimplifierFactory.class,
            ((NamedModule) automatonSimplifier.getSelectedItem()).getName()).getUserModuleDescription()));
  }//GEN-LAST:event_automatonSimplifierChanged

  private void defectiveChanged(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_defectiveChanged
    descDefective.setText(htmlize(
            ModuleSelectionHelper.lookupImpl(DefectiveAutomatonSimplifierFactory.class,
            ((NamedModule) defective.getSelectedItem()).getName()).getUserModuleDescription()));
}//GEN-LAST:event_defectiveChanged

  @Override
  public final void load() {
    automatonSimplifier.setModel(new DefaultComboBoxModel(
            ModuleSelectionHelper.lookupImpls(AutomatonSimplifierFactory.class).toArray()));

    automatonSimplifier.setSelectedItem(ModuleSelectionHelper.lookupImpl(AutomatonSimplifierFactory.class,
            properties.getProperty(DefectiveFactory.PROPERTIES_AUTOMATON_SIMPLIFIER,
            DefectiveFactory.PROPERTIES_AUTOMATON_SIMPLIFIER_DEFAULT)));
    automatonSimplifierChanged(null);

    defective.setModel(new DefaultComboBoxModel(
            ModuleSelectionHelper.lookupImpls(DefectiveAutomatonSimplifierFactory.class).toArray()));

    defective.setSelectedItem(ModuleSelectionHelper.lookupImpl(DefectiveAutomatonSimplifierFactory.class,
            properties.getProperty(DefectiveFactory.PROPERTIES_DEFECTIVE,
            DefectiveFactory.PROPERTIES_DEFECTIVE_DEFAULT)));
    defectiveChanged(null);
  }

  @Override
  public void store() {
    properties.setProperty(DefectiveFactory.PROPERTIES_AUTOMATON_SIMPLIFIER,
            ((NamedModule) automatonSimplifier.getSelectedItem()).getName());

    properties.setProperty(DefectiveFactory.PROPERTIES_DEFECTIVE,
            ((NamedModule) defective.getSelectedItem()).getName());
  }
